
def get_score(l, s):
	c = 0
	for i, e in enumerate(l):
		c += int(e == s[i])
	return c

def check_modulo(l, n):
	mod = [i%n for i in l]
	s = list(set(mod))
	if len(s) == len(l):
		return 1, get_score(sorted(s), s)
	return 0, 0

def check_modulo_sq(l, n):
	mod = [(i*i)%n for i in l]
	s = list(set(mod))
	if len(s) == len(l):
		return 1, get_score(sorted(s), s)
	return 0, 0

def check_modulo_cu(l, n):
	mod = [(i*i*i)%n for i in l]
	s = list(set(mod))
	if len(s) == len(l):
		return 1, get_score(sorted(s), s)
	return 0, 0

